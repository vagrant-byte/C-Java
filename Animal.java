package AnimalClass;

public class Animal {
    public String name;
    public Animal(String name) {
        this.name=name;
    }
}
class Cat extends Animal  {
    public Cat(String name) {
        super(name);
    }
}
class Bird extends Animal {
    public Bird(String name) {
        super(name);
    }
}
class Dick extends Animal {
    public Dick(String name) {
        super(name);
    }
}
